/**
 * Adds two numbers.
 *
 * @since 1.0.0
 * @category Math
 * @param augend The first number in an addition.
 * @param addend The second number in an addition.
 * @returns Returns the total.
 * @example
 *
 * add(6, 4)
 * // => 10
 */
export const add = (augend: number, addend: number) => augend + addend;

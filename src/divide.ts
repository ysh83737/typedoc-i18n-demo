/**
 * Divide two numbers.
 *
 * @since 1.0.0
 * @category Math
 * @param dividend The first number in a division.
 * @param divisor The second number in a division.
 * @returns Returns the quotient.
 * @example
 *
 * divide(6, 4)
 * // => 1.5
 */
export const divide = (dividend: number, divisor: number) => dividend / divisor;
